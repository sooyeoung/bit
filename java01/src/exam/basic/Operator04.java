/* 관계 연산자(Relational Operator)
 * :  >, >=, <, <=, ==, !=
 * - 관계 연산자의 결과 값은 boolean type이다. 
 */
public class Operator04
{
	public static void main(String[] args) 
	{
		int a = 10;
		int b = 20;

		boolean 관계1 = a > b;
		System.out.println("a > b : " + 관계1);

		//  나머지는 여러분이 테스트 하세요...		
	}
}

